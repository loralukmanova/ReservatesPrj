    private void loadRezervatesFromFile(){
        
        Path resourceDirectory = Paths.get("src","resources", RESERVATESTXT);
        File file = resourceDirectory.toFile();
        
        Scanner input = null;
        try {
            input = new Scanner(file);
            
            while (input.hasNext()) {
                Reservate r = new Reservate();
                r.setName(input.next());
                r.setX(input.nextInt());
                r.setY(input.nextInt());
                r.setIconName(input.next());
                r.setImageName(input.next());
                r.setDescription(input.next());
                reservates.add(r);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            input.close();
        }

    }

    private void appendToFile(Reservate r){
        Path resourceDirectory = Paths.get("src","resources", RESERVATESTXT);      
        try {
            Files.write(resourceDirectory, r.toString().getBytes(), StandardOpenOption.APPEND);
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

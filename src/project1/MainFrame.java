    public void loadRezervates(){
        Path resourceDirectory = Paths.get("src","resources");
        String absolutePath = resourceDirectory.toFile().getAbsolutePath();
        for(Reservate r : reservates){
            ImageIcon icn = new ImageIcon(absolutePath+"/"+r.getIconName());
            JLabel rL = new JLabel(icn);
            rL.setBounds(r.getX(), r.getY(), 16, 16);
            rL.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                viewReservate(r);
            }
        });
            mp.add(rL);
            
        }
        mp.repaint();
    }
